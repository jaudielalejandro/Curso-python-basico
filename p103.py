# Lista inicial
numbers = [111, 7, 2, 1]

# Agregar un nuevo número al final con append()
numbers.append(4)
print(numbers)  # Salida esperada: [111, 7, 2, 1, 4]

# Insertar un número en la primera posición (índice 0)
numbers.insert(0, 222)
print(numbers)  # Salida esperada: [222, 111, 7, 2, 1, 4]

# Insertar 333 en la segunda posición (índice 1)
numbers.insert(1, 333)
print(numbers)  # Salida esperada: [222, 333, 111, 7, 2, 1, 4]
